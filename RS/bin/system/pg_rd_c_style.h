#ifndef __PG_RD_C_STYLE_H__
#define __PG_RD_C_STYLE_H__

#include <systemc.h>
#include "parameters.h"

SC_MODULE(pg_rd_c_style) {

    // System signals
    sc_in_clk   clk; // clock
    sc_in<bool> rst; // reset

    // Arbitration signals
    sc_in<sc_uint<N> >  G;  // Grants
    sc_out<sc_uint<N> > P;  // Priorities


    // Internal data structures
    unsigned short int XID, YID, PORT_ID;

    // Initialize seed
    // - Calculates the seed according XID, YID and PORT_ID
    void init();
    unsigned short seed;  // Seed

    // Module's process
    void p_output();
    void p_debug();

    SC_HAS_PROCESS(pg_rd_c_style);
    //////////////////////////////////////////////////////////////////////////////
    pg_rd_c_style(sc_module_name nm, unsigned short int XID, unsigned short int YID,
       unsigned short int PORT_ID) : sc_module(nm),  XID(XID), YID(YID),
        PORT_ID(PORT_ID)
    //////////////////////////////////////////////////////////////////////////////
    {
        init();

        SC_METHOD(p_output);
        sensitive << clk.pos() << rst;

#ifdef DEBUG_PG
        SC_METHOD(p_debug);
        sensitive << clk.pos();
#endif
    }
};
#endif // __PG_RD_C_STYLE_H__
